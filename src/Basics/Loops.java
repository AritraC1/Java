package Basics;

public class Loops {
    public static void main(String[] args) {

        // For Loop
        for (int i = 0; i <=10; i++) {
            System.out.println(i);
        }

        // While Loop
        int a = 0;
        while (a<=10) {
            System.out.println(a);
            a++;
        }

        // Do While Loop
        int b = 0;
        do {
            System.out.println(b);
            b++;
        } while (b<=10);
    }
}
