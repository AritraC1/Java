package Basics;

public class Hello {
    public static void main(String[] args) {
        System.out.println("Hello World");

        // Difference in print and println -
        System.out.println("Hello World!!"); // prints in new line
        System.out.print("Hello World!! This is Java\n"); // sends to next line
        System.out.print("Woah!! This is Java");

        System.out.print("Hello World!! This is Java\nIt is COOL!!"); // '\n' send to next line
    }
}
